package humantransform;

public class Human {
    
    public String name;
    public String gender;
    public int age;
    public double hight;

    public Human(String name, String gender, int age, double hight) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.hight = hight;
    }

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }

    public double getHight() {
        return hight;
    }
    
    
    
    
    

    
}
